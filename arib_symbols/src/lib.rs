pub fn code_point_to_char(cp: u16) -> Option<char> {
    let row = cp >> 8;
    let col = cp & 0xff;
    let (table, base) = match row {
        0x75..=0x76 => (KANJI_TABLE, 0x75),
        0x7a..=0x7e => (SYMBOL_TABLE, 0x7a),
        _ => return None,
    };
    if col < 0x21 || col > 0x7e {
        return None;
    }
    let pos = usize::from((row - base) * 94 + (col - 0x21));
    if pos >= table.len() {
        return None;
    }
    let c = table[pos];
    if c as usize == 0 {
        return None;
    }
    return Some(c);
}
